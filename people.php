		<h2>Intro</h2>
		<p>New York City is the most populous city in the United States, with an estimated record high of 8,622,698 residents as of 2017, incorporating more immigration into the city than outmigration since the 2010 United States Census. More than twice as many people live in New York City as in the second-most populous U.S. city (Los Angeles), and within a smaller area. New York City gained more residents between April 2010 and July 2014 (316,000) than any other U.S. city. New York City's population is about 43% of New York State's population and about 36% of the population of the New York metropolitan area.</p>

		<h2>Overview</h2>

		<p>The diverse population of New York City runs the gamut from some of America's wealthiest celebrities and socialites to homeless people. New York's population, formed by hundreds of thousands of immigrants, has been diverse since the city's founding by the Dutch, and successive waves of immigration from virtually every nation in the world make New York a giant social experiment in cross-cultural harmony.</p>

		<p>The city's ethnic heritage illuminates different neighborhoods throughout the five boroughs. Manhattan's Chinatown remains a vibrant center of New York City's Chinese community, though the very large Chinese community in Flushing, Queens, has rivaled if not eclipsed it in importance, and three other Chinatowns have formed in New York City: the Brooklyn Chinatown in Sunset Park; the Elmhurst Chinatown in Queens; and the Avenue U Chinatown located in the Homecrest section of Brooklyn.</p>

		<h2>Specifics</h2>

		<h3>"Little" countries</h3>
		<p>Traces of the Lower East Side's once-thriving Jewish community still exist amid the gentrified neighborhood's trendy restaurants and bars, but there are Chasidic communities in Borough Park, Crown Heights and Williamsburg, Brooklyn. Harlem has been gentrifying and diversifying and remains a center of African-American culture in New York. East (Spanish) Harlem, though also significantly gentrified, still justifies its reputation as a large Hispanic neighborhood.</p>

		<p>Little known to most tourists are the large Dominican neighborhoods of Hamilton Heights and Washington Heights in upper Manhattan. Brooklyn's Greenpoint is famous for its formerly large and vibrant Polish community, of which only a bit remains, and the Flatbush section - once home to the Brooklyn Dodgers - is today a huge and thriving Caribbean and West Indian section. Queens and Brooklyn are known for being home to many of New York's immigrant groups.</p>

		<h3>Income Statistics</h3>
		<p>New York City has a high degree of income disparity as indicated by its Gini Coefficient of 0.5 for the city overall and 0.6 for Manhattan. In the first quarter of 2014, the average weekly wage in New York County (Manhattan) was $2,749, representing the highest total among large counties in the United States.</p>

		<p>As of 2017, New York City had the highest number of billionaires of any city in the world at 82, with a combined net worth of US$398 billion, including former Mayor Michael Bloomberg. New York also had the highest density of millionaires per capita among major U.S. cities in 2014, at 4.6% of residents. New York City is one of the relatively few American cities levying an income tax (currently about 3%) on its residents.</p>

		<h2>The Accent</h2>
		<p>The New York area has historically been home to a distinctive regional speech pattern called the New York dialect, alternatively known as Brooklynese or New Yorkese. It has generally been considered one of the most recognizable accents within American English.</p>


